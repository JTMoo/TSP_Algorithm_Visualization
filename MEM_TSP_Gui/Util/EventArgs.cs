using System;


namespace MEM_TSP.Gui.Util
{
    /// ****************************************************************************************************************
    /// <summary>
    /// Definieren der Event Argumente
    /// </summary>
    /// ****************************************************************************************************************
    public class EventArgs<T> : EventArgs
    {
        /// ------------------------------------------------------------------------------------------------------------
        /// <summary>
        /// Konstruktor
        /// </summary>
		/// ------------------------------------------------------------------------------------------------------------
        /// <param name="value">Generic Value</param>
		/// ------------------------------------------------------------------------------------------------------------
        public EventArgs(T value)
        {
            Value = value;
        }

        /// ------------------------------------------------------------------------------------------------------------
        /// <summary>
        /// Zugriff auf den Wert
        /// </summary>
		/// ------------------------------------------------------------------------------------------------------------
        public T Value { get; private set; }
    }
}
